package model;

public class MarketImgBean {
	private int mino;
	private String url;
	private int mno;
	public int getMino() {
		return mino;
	}
	public void setMino(int mino) {
		this.mino = mino;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	
	
}
